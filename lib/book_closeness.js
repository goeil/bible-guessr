// Ce fichier décrit l'éloignement d'un livre avec un autre.
// Score de 0 à 100
// 100 = même livre
// 0 = rien à voir

import distanceMatrix from "../data/book_closeness.json";
const matrix = distanceMatrix;

export function getDistance(bookA, bookB) {
  const row = matrix[bookA];
  if (!row) return 0; // livre A inconnu

  const value = row[bookB];
  return value !== undefined ? value : 0; // livre B inconnu → 9
}

